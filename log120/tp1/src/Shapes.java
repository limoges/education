
public class Shapes {
	private int nseq;
	private String name;
	
	public Shapes(int nseq) {
		this.nseq = nseq;
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNseq() {
		return this.nseq;
	}

	public void setNseq(int nseq) {
		this.nseq = nseq;
	}
}
